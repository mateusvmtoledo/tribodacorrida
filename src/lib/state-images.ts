// Função para obter imagem baseada no estado e tipo
export function getStateImage(state: string, index: number = 0): string {
  const images = statePostcardImages[state] || statePostcardImages.SP;
  return images[index % images.length];
}

// Função para obter imagem baseada no tipo de corrida
export function getRaceTypeImage(type: string, index: number = 0): string {
  const images = raceTypeImages[type] || raceTypeImages.rua;
  return images[index % images.length];
}

// Função para gerar imagem consistente baseada no ID da corrida
export function getRaceImage(state: string, raceId: string): string {
  // Usa o ID para gerar um índice consistente
  const hash = raceId.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
  const images = statePostcardImages[state] || statePostcardImages.SP;
  return images[hash % images.length];
}
